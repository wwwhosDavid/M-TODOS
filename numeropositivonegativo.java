public class numeropositivonegativo {

    public static void main(String[] args) {
}
    // Método para verificar numero negativo e positivo
    public static String numeroPositivoNegativo(int numero) {

    if (numero >= 0) {
        return "O numero" + numero + " é positivo";
    } else {
        return "O numero" + numero + " é negativo";
    }    
}
}